import * as Leaflet from 'leaflet';

export class ColoredIcons{
  static getColoredIconByIndex(colorIndex): Leaflet.Icon{
    switch (colorIndex){
      case 0: return blueIcon;
      case 1: return redIcon;
      case 2: return greenIcon;
      case 3: return orangeIcon;
      case 4: return violetIcon;
      case 5: return greenIcon;
      case 6: return blackIcon;
      case 7: return goldIcon;
      case 8: return yellowIcon;
    }
  }

  static getColorByIndex(colorIndex) {
    switch (colorIndex){
      case 0: return '#3274A3';
      case 1: return '#982E40';
      case 2: return '#31882A';
      case 3: return '#98652E';
      case 4: return '#742E98';
      case 5: return '#6B6B6B';
      case 6: return '#313131';
      case 7: return '#C1A32D';
      case 8: return '#988F2E';
    }
  }
}

